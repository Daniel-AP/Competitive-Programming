void solve() {

    int n; cin >> n;

    vector<int> a(n);
    for(int i = 0; i < n; i++) cin >> a[i];

    vector<int> prefix(n+1);
    partial_sum(a.begin(), a.end(), prefix.begin()+1);

    int ans = -INF;

    for(int i = 1; i <= n; i++) {
        for(int j = i; j <= n; j++) {
            ans = max((prefix[j]-prefix[i-1])/(j-i+1), ans);
        }
    }

    cout << ans << '\n';
    
}

signed main() {

    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);

    int t = 1; cin >> t;

    while(t--) solve();

    return 0;

}