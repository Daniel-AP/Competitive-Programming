void solve() {

    int n; cin >> n;

    vector<int> p(n);

    iota(all(p), 1);
    reverse(all(p));
    
    rotate(p.begin(), p.end()-1, p.end());

    for(int x: p) cout << x << ' ';
    cout << '\n';

}

signed main() {

    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);

    int t = 1;
    cin >> t;

    while(t--) solve();

    return 0;

}