void solve() {

    int n; cin >> n;

    vector<int> ans(2*n);
    ans[0] = ans[n] = n;

    for(int i = 1; i < n; i++) ans[i] = n-i;
    for(int i = n+1; i < 2*n; i++) ans[i] = i-n;

    for(int x: ans) cout << x << ' ';
    cout << '\n';
    
}

signed main() {

    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);

    int t = 1; cin >> t;

    while(t--) solve();

    return 0;

}